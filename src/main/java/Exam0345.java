


public class Exam0345 { // 자바 프로그램 Class 선언, Exam01 클래스 선언
    public static void main(String[] args) { // 자바 프로그램이 가장 먼저 실행되는 진입점(main 메서드)

        // 1. 외부 반복문: 트리의 전체 줄(행, Row)을 제어합니다. (총 4줄 출력)
        for (int i = 1; i <= 4; i++) { // 별 트리 4줄 구성, i 1~4까지 1씩 증가 4번 반복

            // 2. 첫 번째 내부 반복문: 각 줄 앞부분의 '공백'을 출력.
            // 행(i)이 증가할수록 공백이 3, 2, 1, 0으로 줄어야 들어야 '4(현재 줄 번호) - i(공백 개수)' 조건식을 사용합니다.
            for (int j = 1; j <= 4 - i; j++) {
                System.out.print(" "); // println을 쓰지 않고 가로로 이어서 공백을 출력
            }

            // 3. 두 번째 내부 반복문: 트리 모양을 만드는 '별(*)'을 출력합니다.
            // 행(i)이 증가할수록 별이 1, 3, 5, 7개(홀수)로 늘어나야 하므로 '2 * i - 1' 조건식을 사용합니다.
            for (int k = 1; k <= 2 * i - 1; k++) {
                System.out.print("*"); // 가로로 이어서 별을 출력
            }

            // 4. 공백과 별 출력이 모두 끝나면, 다음 줄 출력을 위해 개행(줄바꿈)을 수행합니다.
            System.out.println();

        } //바깥쪽 for문 끝
    }//메인 메서드 끝
}// Exam01 클래스 끝