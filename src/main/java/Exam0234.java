







public class Exam0234 { // 자바 프로그램의 기본 단위인 클래스를 선언하며, 문제에서 요구한 'Exam01'로 이름을 지정합니다.
    public static void main(String[] args) { // 자바 프로그램의 진입점(Entry Point)이 되는 main 메서드

        // 1. 전체 줄(행)을 제어하는 바깥쪽 반복문
        // 별 트리가 총 4줄로 구성되어 있으므로, 변수 i는 1부터 4까지 1씩 증가하며 총 4번 반복합니다.
        for (int i = 1; i <= 4; i++) {

            // 2. 각 줄의 '공백(빈칸)'을 먼저 출력하는 안쪽 반복문
            // 공백은 위에서부터 3개, 2개, 1개, 0개 순으로 줄어들어야 합니다.
            // 따라서 '전체 줄 수(4) - 현재 줄 번호(i)' 공식을 사용하여 공백의 개수를 정확히 제어합니다.
            for (int j = 1; j <= 4 - i; j++) {
                System.out.print(" "); // println이 아닌 print를 사용하여 줄바꿈 없이 가로로 이어서 공백을 출력합니다.
            }

            // 3. 각 줄의 '별(*)'을 이어서 출력하는 안쪽 반복문
            // 별은 위에서부터 1개, 3개, 5개, 7개 순으로 홀수 개수만큼 늘어납니다.
            // 따라서 홀수를 만드는 수학 공식인 '2 * 현재 줄 번호(i) - 1'을 조건식으로 사용하여 별의 개수를 제어합니다.
            for (int k = 1; k <= 2 * i - 1; k++) {
                System.out.print("*"); // 줄바꿈 없이 가로로 이어서 별을 출력합니다.
            }

            // 4. 다음 줄로 넘어가기
            // 위의 두 안쪽 반복문(공백, 별)이 끝나면 해당 줄의 출력이 완료된 것이므로,
            // 다음 줄(i가 증가한 상태)을 출력하기 위해 아무 내용 없이 줄바꿈(Enter) 역할만 수행합니다.
            System.out.println();

        } // 바깥쪽 for문의 끝
    } // 메인 메서드의 끝
} // Exam01 클래스의 끝